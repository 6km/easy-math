module.exports = {
    sin(n: number) {
        return Math.sin(n);
    },

    cos(n: number) {
        return Math.cos(n);
    },

    tan(n: number) {
        return Math.tan(n);
    },

    acos(n: number) {
        return Math.acos(n);
    },

    asin(n: number) {
        return Math.asin(n);
    },

    acosh(n: number) {
        return Math.acosh(n);
    },

    atan(n: number) {
        return Math.atan(n);
    },

    atan2(y: number, x: number) {
        return Math.atan2(y, x);
    }
};